# Create a new pet
my_pet = Pet("Buddy")

# Check initial status
my_pet.get_status()

# Let's play with the pet
my_pet.play()
my_pet.get_status()

# Feed the pet
my_pet.eat()
my_pet.get_status()

# Teach some tricks
my_pet.train("roll over")
my_pet.train("fetch")
my_pet.show_tricks()

# Let the pet sleep
my_pet.sleep()
my_pet.get_status()